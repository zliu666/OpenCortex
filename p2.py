# 冒泡排序算法实现（包含优化版本）


def bubble_sort(arr):
    """
    冒泡排序算法 - 优化版本（提前终止）
    
    算法思想：
    1. 通过相邻元素的比较和交换，让大元素"冒泡"到数组末尾
    2. 每一轮会将当前未排序部分的最大元素移到正确位置
    3. 如果某一轮没有发生交换，说明数组已经有序，可以提前结束
    
    时间复杂度：
    - 最好情况：O(n) - 数组已排序
    - 最坏情况：O(n²) - 数组逆序
    - 平均情况：O(n²)
    
    空间复杂度：O(1) - 原地排序
    
    参数:
        arr: 待排序的列表（可变对象，会原地修改）
    
    返回:
        排序后的列表（返回原列表的引用）
    """
    n = len(arr)
    
    # 外层循环：控制排序的轮数
    # 每一轮确定一个元素的最终位置（从后往前）
    for i in range(n):
        # swapped 标记：记录本轮是否发生了交换
        # 优化作用：如果本轮没有交换，说明数组已经有序，可以提前结束
        swapped = False
        
        # 内层循环：进行相邻元素的比较和交换
        # 范围：0 到 n-i-1（每轮后部已经有序的元素不需要再比较）
        for j in range(0, n - i - 1):
            # 比较相邻元素：如果前一个元素大于后一个元素，则交换
            # 这样大的元素会逐步"冒泡"到数组末尾
            if arr[j] > arr[j + 1]:
                # Python 的元组解包语法实现交换
                # 不需要临时变量，简洁高效
                arr[j], arr[j + 1] = arr[j + 1], arr[j]
                # 标记本轮发生了交换
                swapped = True
        
        # 优化核心：如果本轮没有发生任何交换，说明数组已经有序
        # 提前终止排序，避免不必要的遍历
        if not swapped:
            break
    
    return arr


if __name__ == "__main__":
    """
    测试代码：验证冒泡排序的正确性和优化效果
    """
    
    # 测试用例集合：包含各种边界情况和典型场景
    test_arrays = [
        [64, 34, 25, 12, 22, 11, 90],  # 普通无序数组
        [5, 1, 4, 2, 8],                 # 较小的无序数组
        [1, 2, 3, 4, 5],                 # 已排序数组（测试优化效果）
        [5, 4, 3, 2, 1],                 # 逆序数组（最坏情况）
        [42],                             # 单个元素数组
        [],                               # 空数组
        [3, 3, 3, 3],                     # 所有元素相同
        [2, 1],                          # 只有两个元素
    ]
    
    # 遍历所有测试用例
    for arr in test_arrays:
        # 复制原数组用于显示，因为排序会原地修改
        original = arr.copy()
        # 执行冒泡排序
        sorted_arr = bubble_sort(arr)
        # 打印排序前后的对比结果
        print(f"原数组: {original}")
        print(f"排序后: {sorted_arr}")
        print("-" * 40)  # 分隔线，方便阅读
