def t(key: str, lang: str = "en") -> str:
    """Translate a command description.

    Args:
        key: Command name (e.g., "help", "exit")
        lang: Language code ("en" or "zh")

    Returns:
        Translated description, or the key itself if not found.
    """
    lang_dict = COMMAND_DESCRIPTIONS.get(lang, COMMAND_DESCRIPTIONS.get("en", {}))
    return lang_dict.get(key, key)
